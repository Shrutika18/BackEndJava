package Basics;

public class MathOperations
{
    public static void main(String[] args)
    {
        int a = 100;
        int b = 20;
        int result;

        result = a +b;
        System.out.println("Result is" + result);

        result = a*b;
        System.out.println("Result is" + result);

        result = a/b;
        System.out.println("Result is" + result);

        result = a-b;
        System.out.println("Result is" + result);
    }
}
